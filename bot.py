async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    context.user_data["index"] = 0
    keyboard = [[InlineKeyboardButton("–ù–∞—á–∞—Ç—å", callback_data="start_questions")]]
    await update.message.reply_text(WELCOME_TEXT, reply_markup=InlineKeyboardMarkup(keyboard))

async def handle_buttons(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()

    if "index" not in context.user_data:
        context.user_data["index"] = 0

    if query.data == "start_questions":
        await send_question(query, context)

    elif query.data == "next":
        context.user_data["index"] += 1
        await send_question(query, context)

    elif query.data == "prev":
        context.user_data["index"] -= 1
        await send_question(query, context)

    elif query.data == "finish":
        await query.edit_message_text(FINAL_TEXT, reply_markup=InlineKeyboardMarkup([
            [InlineKeyboardButton("Restart", callback_data="restart")]
        ]))

    elif query.data == "restart":
        context.user_data["index"] = 0
        await query.edit_message_text(WELCOME_TEXT, reply_markup=InlineKeyboardMarkup([
            [InlineKeyboardButton("–ù–∞—á–∞—Ç—å", callback_data="start_questions")]
        ]))

    elif query.data == "back_to_start":
        context.user_data["index"] = 0
        await send_question(query, context)


async def send_question(query, context):
    index = context.user_data["index"]

    # –ï—Å–ª–∏ –¥–æ—à–ª–∏ –¥–æ –∫–æ–Ω—Ü–∞
    if index >= len(QUESTIONS):
        index = len(QUESTIONS) - 1
        context.user_data["index"] = index

    # –ï—Å–ª–∏ —É—à–ª–∏ –≤ –º–∏–Ω—É—Å
    if index < 0:
        index = 0
        context.user_data["index"] = index

    buttons = []
    if index > 0:
        buttons.append(InlineKeyboardButton("‚¨ÖÔ∏è –ù–∞–∑–∞–¥", callback_data="prev"))
    if index < len(QUESTIONS) - 1:
        buttons.append(InlineKeyboardButton("‚û°Ô∏è –î–∞–ª–µ–µ", callback_data="next"))
    else:
        buttons.append(InlineKeyboardButton("‚úÖ –ó–∞–≤–µ—Ä—à–∏—Ç—å", callback_data="finish"))

    # –í—Ç–æ—Ä–∞—è —Å—Ç—Ä–æ–∫–∞ –∫–Ω–æ–ø–æ–∫
    extra_buttons = [
        InlineKeyboardButton("üîÑ Restart", callback_data="restart"),
        InlineKeyboardButton("üîô –í–µ—Ä–Ω—É—Ç—å—Å—è –≤ –Ω–∞—á–∞–ª–æ", callback_data="back_to_start")
    ]

    await query.edit_message_text(
        text=QUESTIONS[index],
        reply_markup=InlineKeyboardMarkup([buttons, extra_buttons])
    )

